class CGR3QueryRequest(BaseModel):
    """Request body for the CGR3 iterative reasoning query."""

    query: str = Field(
        min_length=3,
        description="Natural language question to answer via iterative reasoning.",
    )
    mode: Literal["local", "global", "hybrid", "naive", "mix"] = Field(
        default="hybrid",
        description=(
            "Retrieval mode used in each CGR3 iteration. "
            "'hybrid' is recommended for multi-hop questions."
        ),
    )
    max_iterations: int = Field(
        default=3,
        ge=1,
        le=10,
        description=(
            "Maximum Retrieve→Rank→Reason iterations. "
            "The loop stops early when the LLM judges context sufficient."
        ),
    )
    top_k: Optional[int] = Field(
        default=None,
        ge=1,
        description="Number of entities/relations to retrieve per iteration.",
    )
    include_references: bool = Field(
        default=True,
        description="If True, include source citations in the response.",
    )

    @field_validator("query", mode="after")
    @classmethod
    def strip_query(cls, v: str) -> str:
        return v.strip()


class CGR3QueryResponse(BaseModel):
    """Response from a CGR3 iterative reasoning query."""

    response: str = Field(description="The final answer generated by CGR3.")
    references: Optional[List[ReferenceItem]] = Field(
        default=None,
        description="Source citations (present when include_references=True).",
    )


class RelationContextData(BaseModel):
    """Parsed relation context (rc) for a single edge."""

    supporting_sentences: List[str] = Field(
        default_factory=list,
        description="Direct verbatim quotes from source documents supporting this relationship.",
    )
    temporal_info: Optional[str] = Field(
        default=None,
        description="Validity period or timestamp (e.g., 'Q4 2026', 'since 2020').",
    )
    quantitative_data: Optional[str] = Field(
        default=None,
        description="Numerical data: amounts, percentages, counts.",
    )

    @field_validator("quantitative_data", "decision_trace", "provenance", "temporal_info", mode="before")
    @classmethod
    def coerce_to_str(cls, v):
        if isinstance(v, list):
            return ", ".join(str(item) for item in v)
        return v

    @field_validator("supporting_sentences", mode="before")
    @classmethod
    def coerce_to_list(cls, v):
        if isinstance(v, str):
            return [v]
        return v
    decision_trace: Optional[str] = Field(
        default=None,
        description="Rationale, exception, or approval behind this relationship.",
    )
    provenance: Optional[str] = Field(
        default=None,
        description="Source reference: Slack thread, transcript timestamp, document section.",
    )
    approved_by: Optional[str] = Field(
        default=None,
        description="Entity name of the approver (e.g., 'VP_Smith', 'Finance_Team').",
    )
    approved_via: Optional[str] = Field(
        default=None,
        description="Channel: 'slack', 'zoom', 'email', 'in_person', 'jira', 'system'.",
    )
    valid_from: Optional[str] = Field(
        default=None,
        description="ISO-8601 date when this decision became effective (e.g., '2024-08-14').",
    )
    valid_until: Optional[str] = Field(
        default=None,
        description="ISO-8601 date when this decision expires (e.g., '2024-12-31').",
    )
    policy_ref: Optional[str] = Field(
        default=None,
        description="Policy this decision follows or overrides (e.g., 'DiscountPolicy_Standard').",
    )
    confidence_score: float = Field(
        default=1.0,
        description="Extraction reliability (0.0–1.0).",
    )


class EdgeContextResponse(BaseModel):
    """Relation context for a specific graph edge."""

    src_id: str = Field(description="Source entity (head).")
    tgt_id: str = Field(description="Target entity (tail).")
    has_context: bool = Field(
        description="True if a RelationContext was stored for this edge."
    )
    relation_context: Optional[RelationContextData] = Field(
        default=None,
        description="Parsed relation context (null if none was extracted).",
    )


