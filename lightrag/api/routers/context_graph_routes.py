"""
Context Graph API routes — CGR3 reasoning and relation-context inspection.

Provides endpoints that are exclusive to ContextGraph mode:

  POST /cgr3/query               — Iterative Retrieve→Rank→Reason query
  GET  /graph/edge/context       — Relation context for a specific edge
  GET  /graph/entity/edges-with-context — All context-enriched edges for an entity

All endpoints return HTTP 503 when the server is running plain LightRAG (i.e.
USE_CONTEXT_GRAPH is not enabled), so clients can detect capability at runtime.
"""

from __future__ import annotations

from typing import Any, Dict, List, Literal, Optional

from fastapi import APIRouter, Depends, HTTPException, Query
from pydantic import BaseModel, Field, field_validator

from lightrag.api.utils_api import get_combined_auth_dependency
from lightrag.api.routers.query_routes import ReferenceItem
from lightrag.utils import logger

